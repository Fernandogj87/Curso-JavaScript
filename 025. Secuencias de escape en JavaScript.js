/*
Secuencias de escape en JavaScript.
*/

/*
Código | Resultado
------------------  
\'      Comilla Simple
\"      Comilla Doble
\\      Barra invertida
\n      Línea Nueva
\r      Retorno de Carro
\t      Tabulación
\b      Retroceso
\f      Salto de Página
 */

let objetivo;

objetivo = "Aprender \'JavaScript\'";
console.log(objetivo);

objetivo = "Aprender \"JavaScript\"";
console.log(objetivo);

objetivo = "Aprender \\ JavaScript";
console.log(objetivo);

objetivo = "Aprender \n JavaScript";
console.log(objetivo);
