/*
Concatenar cadenas de caracteres.
*/

let nombreCompleto = "Fernando" + "Gamero"; // Sin espacio entre las cadenas
console.log(nombreCompleto);

nombreCompleto = "Fernando " + "Gamero"; // Con espacio
console.log(nombreCompleto);

nombreCompleto = "Fernando" + " Gamero"; // Con espacio
console.log(nombreCompleto);

nombreCompleto = "Fernando" + " " + "Gamero"; // Con espacio
console.log(nombreCompleto);
